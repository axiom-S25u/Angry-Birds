#include <vector>

/*
all coords are RELATIVE to the target zone on the right side of screen
  x=0 = left edge of where the buildings/pigs spawn
  y=0 = FLOOR level (the brown ground)
  (+x) = further RIGHT
  (+y) = UP in the air

brickdef = {x, y, width, height, hp}
  - x, y = BOTTOM-LEFT corner of the brick rectangle
  - width, height = size of the brown block
  - hp = health points (how much dmg before it breaks)

pigdef = {x, y}
  - x, y = CENTER of the green pig icon (32x32 pixels)
  - pigs are SimplePlayer cubes with green colors 
  - 60 hp default, dies when hp <= 0

leveldef = {"Name", birdCount, {bricks}, {pigs}}
  - Name = level display name
  - birdCount = how many birds player gets
  - bricks = vector of BrickDef (the brown obstacles)
  - pigs = vector of PigDef (the green targets to kill)

in-game these get offset by:
  baseX = screenWidth - 240.0f  (right side of screen)
  floorY = 60.0f  (above bottom of screen)
yes i had to explain this since NO one would understand
most documentation i will ever write in my life
*/
